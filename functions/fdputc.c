int fdputc(int c, int fd)
{
	asm("LOAD_IMMEDIATE_rax %1"
	"LOAD_EFFECTIVE_ADDRESS_rdi %8"
	"LOAD_INTEGER_rdi"
	"LOAD_EFFECTIVE_ADDRESS_rsi %16"
	"LOAD_IMMEDIATE_rdx %1"
	"SYSCALL");
}
